// Cell Functions

function Clean(){
	for (var i = 0; i < cellNumberX; i++) {
		cells[i] = new Array();

		for (var j= 0; j < cellNumberY; j++) {
			cells[i][j] = 0;
		}
	}

	document.getElementById("generation").innerHTML = generation = 0;
	population = 0;
}

// Pre-fills the Board
function preFill(cells, pre){
	Clean();

	pre.forEach(function(point) {
		cells[point[0]][point[1]] = 1;
	});
}

function countLiveCells(cells){
	var count = 0;
	cells.forEach(function(row){
		row.forEach(function(cell){
			if(cell == 1) count++;
		});
	});

	return count;
}

// Getting map
function CREATE_MAP(cells){
	var MAP = [];
	cells.forEach(function(row,x){
		row.forEach(function(cell,y){
			if(cell == 1) MAP.push([x,y]);
		});
	});

	var string = "";
	MAP.forEach(function(cell){
		string += "[" + cell[0] + "," + cell[1] + "],"; 
	});

	return string;
}

